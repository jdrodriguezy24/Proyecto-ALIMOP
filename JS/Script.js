//Esta funcion es para la informacion en espa√±ol
function toggleInformacion(id) {
  var informacion = document.getElementById(id);
  if (informacion.style.display === "none") {
    informacion.style.display = "block";
  } else {
    informacion.style.display = "none";
  }
}


